"""
Given a list of up to four distinct integers between 1 and 10 (inclusive), the task is to find a list of three
integers (also between 1 and 10, inclusive) such that all the original numbers can be generated by summing *subsets*
of these three numbers.  For example, if the input is [1, 3, 7, 8], a valid solution is [1, 2, 5], because:

* 1 = 1
* 3 = 1 + 2
* 7 = 2 + 5
* 8 = 1 + 2 + 5

Print the list of three integers (x) that satisfies the above conditions.
"""

# Data
numbers = [7, 8, 9, 10]
# End of data

# Import libraries
from cpmpy import *
import numpy as np
import json


def increasing(args):
    """
    Ensure that the values in args are increasing.
    """
    return [args[i - 1] <= args[i] for i in range(1, len(args))]


model = Model()

m = len(numbers)
n = 3

x = intvar(1, 10, shape=n, name="x")
# coefficient matrix
tmp = boolvar(shape=(m, n), name="tmp")

for i in range(m):
    model += [sum([tmp[i, j] * x[j] for j in range(n)]) == numbers[i]]

# <SYMMETRY_BREAKING_CONSTRAINT_START>
# model += [increasing(x)]
# <SYMMETRY_BREAKING_CONSTRAINT_END>

model.solve()

solution = {
    "x": x.value().tolist()
}

print(json.dumps(solution))
# End of CPMPy script
